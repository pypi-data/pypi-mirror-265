if __name__ == "__main__":
    import urllib.request

    # generate objects above by processing official X11 named colors (standardized to hyphenated
    # lowercase and dropping duplicates)
    with urllib.request.urlopen("https://gitlab.freedesktop.org/xorg/app/rgb/raw/master/rgb.txt") as f:
        content = f.read().decode("utf-8")
        # remove tabs and split
        COLORS = content.replace("\t", "").split("\n")
        # all RGB triplets are in the first 11 chars (rrr ggg bbb|color name)
        unique_names = set()
        unique_colors = dict()
        for color in COLORS:
            rgb, name = color[:11], color[11:].strip()
            # skip if either is empty
            if not rgb or not name:
                continue
            # normalize formatting of names, e.g.
            # WhiteSmoke -> white-smoke
            # ghost white -> ghost-white
            normalized_name = ""
            for i, c in enumerate(name):
                # capitalized char after first => word separation, so add dash
                if i > 0 and c.isupper():
                    normalized_name += "-"
                normalized_name += c
            # also stick to one spelling of grey / gray...
            normalized_name = normalized_name.replace(" ", "-").lower().replace("grey", "gray")
            if normalized_name not in unique_names:
                unique_names.add(normalized_name)
                unique_colors[normalized_name] = f"({','.join(rgb.split())})"

    # write to file
    with open("_x11.py", "w") as f:
        print("import typing\n", file=f)
        # create COLOR type
        print("Color = typing.Literal[", file=f)
        for name in unique_colors.keys():
            print(f'\t"{name}",', file=f)
        print("]\n", file=f)
        # create color dict
        print("COLORS: dict[COLOR, tuple[int, int, int]] = {", file=f)
        for name, rgb in unique_colors.items():
            print(f'\t"{name}": {rgb},', file=f)
        print("}", file=f)
